<a>Pollution is a major problem for the earth and a danger to our health. There are many different types of pollution such as:<br>

<br><a><b>-Air pollution</b><br> For further information, visit our: <u><a href="./air pollution.html">What is Air pollution?</u></a>

<br><a><b>-Water pollution</b><br> Water pollution is the contamination of water to the extent of making the water a hazard to use for tasks such as drinking,cleaning or cooking. There are many pollutants of water including but not limited to oil, plastic and chemicals(such as pesticides).</a>
