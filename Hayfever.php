<h2><center>How to deal with hay fever in pollen season.</center></h2>	
<center><iframe width="560" height="315" src="https://www.youtube.com/embed/xBlLvXkTjcI" title="YouTube video player" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture; web-share" allowfullscreen></iframe></center>
<article class="Definition">
Hayfever, also known as seasonal allergic rhinitis, is an allergic reaction to pollen. It can be triggered by allergens in the air around us, like grass, tree, plant pollen and mould. Between May and July, during the traditional growing season, trees and plants create more pollen, and this, together with warmer more humid temperatures and winds, can bring about a spike in our symptoms. 
<br>
<center><br><b>Symptoms can range from:</b><br>
Frequent sneezing<br>
-Runny or blocked nose<br>
-Itchy, red or watery eyes (allergic conjunctivitis)<br>
-An itchy throat, mouth, nose and ears<br>
-Cough, caused by postnasal drip (mucus dripping down the throat from the back of the nose)<br></center>

<center><br><b>Less commonly, you may also experience:</b><br>
-The loss of your sense of smell (anosmia)<br>
-Facial pain (caused by blocked sinuses)<br>
-Headaches
-Earache<br>
-Tiredness and fatigue<br></center>

<center><br><b>Minimizing symptoms</b><br>
-Remove outdoor clothing and shower or wash your hair following high pollen exposure<br>
-Close windows at night<br>
-Vacuum regularly, preferably with a high-efficiency particle arresting (HEPA) filter in place<br>
-Avoid keeping flowers in the house.<br>
-Avoid drying washing outdoors when the pollen count is high or when cutting the grass, as this can bring pollen into the house.<br>
-Avoid walking in grassy, open spaces, particularly during the early morning and early evening<br>
-Keep an eye on the weather forecast for information about pollen count levels and avoid going outdoors when the pollen count is Medium or High<br>
-Install pollen filters in your car if they are not already fitted<br>
-Drive with windows closed<br>
-Consider wearing wrap-around sunglasses to stop pollen getting in your eyes when outdoors<br></center>

<center>
<br><b>Dealing with symptoms</b><br>
Treatment options for hay fever include:<br>
Antihistamines – which help to prevent an allergic reaction<br>
Corticosteroids (steroids) – which help to reduce inflammation and swelling<br>
</center>
