   <a>Air pollution is the contamination of the air via pollutants such as carbon monoxide and particulate matter. Air pollution is important as it can greatly impact our health. Studies show a greater concentration of air pollution can result in conditions sucn as athsma, lung cancer and breathing difficulties. </a>
   
   <br><b><a>How does air pollution effect asthma?</a></b><br>
   <a>Ozone is a common gas found to be a common pollutant of the atmosphere, it is one of contributions of smog/fog we sometimes see in high areas of pollution. Ozone can be irritating towards the lungs, as it makes it harder for you to breath deeply as well as reduce lung function. Ozone is typically more common in days of high sunlight and low wind. </a>
   <a>Other triggers of asthma within air pollution can be small airborne particles found in the atmopshere, found in smoke,haze and airborne dust. </a><br>
   
   <br><a><b>Outdoor air pollution:</b></a><br>
   <a>The concentration of air pollution is more likely to be higher in the summer, if you want to go out, here are our tips:<br>
   -Limit outdoor activities and exercise so you avoid breathing in too much polluted air.<br>
   -Go out earlier in the day when air quality tends to be better.<br>
   -Stay on quieter, back streets if possible, avoiding areas where there’s a lot of traffic.<br>
   -Walk on the inside of the pavement because pollution levels are lower the further you are from the traffic.<br>
   -Keep your car windows closed if you’re driving, especially if you’re driving in slow-moving traffic.<br>
   -Be prepared by checking pollution levels in your area.
   </a><br>
  
   <br><a><b>Lowering outdoor air pollution:</b></a><br>
   <a>
   -Using public transport instead of driving when we can.<br>
   -Walking or cycling if possible, particularly for short trips. This will cut down air pollution and keep us active too.<br>
   -Not idling while stationary if we’re driving. This means not leaving the engine going when you’re pulled in somewhere. <br>
   -Buying cars with lower emissions.<br>
   -Getting cars serviced regularly, including checking the tyres<br> 
   -Avoiding using wood burning stoves or other fossil fuels for heating.<br>
   </a><br>
   
   
   <br><a><b>Indoor air pollution</b></a><br>
   <a>Air pollution can occur within your home. Causes can be:<br> 
    -Household cleaners and air-freshening sprays or devices<br>
    -Fuel-burning heat sources (such as a wood-burning stove)<br>
    -Smoke from cooking, candles, fireplaces or tobacco<br>
    -Toxic fumes that are “off-gassing” from new products (new furniture and new carpet)<br>
    -Attached garages that store cars, motorcycles or lawnmowers (can add carbon monoxide to your air)<br>
    -Building and paint products (paints, adhesives, solvents)<br>
    -Pesticides (such as treatments for cockroaches and fleas)<br>
    -Radon (a gas that comes from the ground and enters a home and can rise to dangerous levels)<br>
    -Humidity that allows mold to grow<br>
    -Cosmetics, perfumes and hair sprays</a><br>

    <br><a><b>How to reduce indoor air pollution:</b></a><br>
    <a>-Remove or reduce allergens<br>
      -Do not smoke tobacco products in your home<br>
      -Prevent mold growth by lowering the humidity in your home by using exhaust fans in kitchens, bathrooms and laundry rooms (or open a window if necessary)<br>
      -If you live in a humid area, consider getting a dehumidifier for your home<br>
      -Increase air flow to give your house better ventilation (open windows, doors)<br>
      -Store harmful products in a shed that is not attached to your home<br>
      -Avoid using scented candles or odor-hiding fragrances<br>
      -Install and check carbon monoxide alarms and radon alarms</a><br>
    
