<article><b>During the power outage<br></b>-Report the outage to your electricity network provider.<br>-Conserve power, by turning appliances off. <br>-Use torches instead of candles(fire safety).<br>-During a winter power outage, try and stay warm, close any doors and windows.<br>-Close fridges and freezers, as these appliances tend to stay cold after many hours.</article>
