<article class="Definition">Find yourself in the middle of a snowstorm? Here are our tips on how to survive: </article>
<br>
<article><b>-Reduce body heat loss.</b> Dress in layers,keeping yourself covered this is to prevent wind and exposure to the cold as well as trapping body heat within those layers.</article>
 <article><b>-Do not eat snow!</b> Eating snow will result in a loss of body temperature as your body tries to eat snow.</article>
 <article><b>-Do not sweat.</b> Sweating will result in mositure loss. Sweating is also the body's mechanism to stay cool which you do not want to do in a snow storm, for this reason stay dry.</article>
<article><b>-Keep your clothing loose.</b> You do not want to wear clothing that is not too tight, as this could disturb circulation of warm blood.</article>
<article><b>-Check your fire alarms and smoke detectors.</b> Fires often happen in winter due to the avid use of heating equipment, make sure your fire alarms and smoke detectors function properly to avoid the risk of a fire.</article>
<article><b>-Keep flashlights and torches on you.</b> You may experience a power outage during the winter, due to the days in winter tending to be much winter, I would advice you to keep a flashlight readily accessible and available.</article>
<article><b>-Close off any doors.</b></article>
<article><b>-Keep your faucets on.</b> Allowing your water pipes to freeze over, will result in the lack of water supply, to avoid this make sure to keep your faucets on in a snow storm.</article>

