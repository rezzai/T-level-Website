<article class="Definition">Climate change is a shift in the patterns of the climate. It can cause irregular weather patterns such as frequent storms. The effects of climate change range from but are not limited to:  <b>rising sea levels; shrinking mountain glaciers; accelerating ice melt in Greenland, Antarctica and the Arctic; and shifts in flower/plant blooming times.</b> Climate change can also cause <b>global warming</b> in which the earth is "warming up". This effect can occur naturally however it is known that global warming is acclerated by the burning of <b>fossil fuels</b>. Fossil fuels such as coal and oil release <b>greenhouse gases</b>, greenhouse gases are gases that helps trap the heat within the sun's atmosphere. </article>
