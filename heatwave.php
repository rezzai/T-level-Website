<article class="Definition">A heatwave is a prolonged period of time, where there is extreme heat. Due to global warmings, the probabilty of a heatwave increases. A heatwave can cause problems such as  heat rashes, heat stroke and severe dehydration. <b>Our top tips for surviving a heatwave is: </b> </article>
	
<br>
<list> <b>-Stay Hydrated!</b> Drink some water, staying dehydrated can cause overheating of your body as well as heat rash and heat stroke! <b>(avoid alcohol, caffeine and hot drinks)</b> </list>
<br>
<list> <b>-Keep yourself cool! </b> Do this by taking a cold shower/bath,having cold food and drinks! </list>
<br>
<list><b>-Stay out the heat.</b> Take precaution when going outside, when having to go outside, make sure to stay in a shaded area. <b>Do not forget to wear light clothes and sunscreen,also make sure to not do heavy exercise while you are out in the heat!</b></list>
<br>
<list><b>-Cool your home down!</b> Invest in electric fans and make sure to open your windows at night, closing them during the day! Maintaing temperature of your home can be ensured with regular checks,  the ideal temperature of your house should be below 35 degrees!</list>

