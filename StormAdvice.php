<center><article><b>Before the storm</b><br>To increase your survivability during a storm, you will need to prepare beforehand.<br>-Close secure windows and doors.<br>-Secure loose objects such as ladders.<br>-Prepare an emergency kit.</article></center>

<br>
<center><article><b>During the storm</b><br>-Try to limit your use of electricity, only plug in the neccessary appliances.<br>-When going out, avoid trees and buildings!<br>-Avoid driving if possible.<br>-Keep your doors and windows shut, avoid opening them if possible!<br>-Try and stay indoors.</article></center>

